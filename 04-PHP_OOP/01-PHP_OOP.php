PHP What is OOP?
=================

OOP stands for Object-Oriented Programming.

Procedural programming is about writing procedures or functions that 
perform operations on the data, while object-oriented programming is 
about creating objects that contain both data and functions.

OOP has several advantages over procedural programming:

-  faster and easier to execute
-  provides a clear structure for the programs
-  helps to keep the PHP code DRY "Don't Repeat Yourself", 
   and makes the code easier to maintain, modify and debug
-  makes it possible to create full reusable applications 
   with less code and shorter development time



PHP - What are Classes and Objects ?
=====================================

Classes and objects are the two main aspects of OOP.

Look at the following illustration to see the difference between 
class and objects:

Class       |    Objects
----------  |    -----------
Fruits      |     apple
            |     banana
            |     mango


Class       |    Objects
----------  |    -----------
Car         |    volvo
            |    audi
            |    toyota

            
So, a class is a template for objects, and an object is 
an instance of a class.

When the individual objects are created, they inherit 
all the properties and behaviors from the class, but 
each object will have different values for the properties.  -->